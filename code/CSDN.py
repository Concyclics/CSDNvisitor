def getUrls(url):

    resp = requests.request("GET", url, data=payload, headers=getHeaders())
    resp.encoding=resp.apparent_encoding
    html_source = resp.text
    urls = re.findall("https://[^>\";\']*\d",html_source)
    new_urls=[]
    for url in urls:
        if 'details' in url:
            if url not in new_urls:
                new_urls.append(url)
    return new_urls


